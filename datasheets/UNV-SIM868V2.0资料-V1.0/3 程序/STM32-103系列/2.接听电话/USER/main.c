/**********************************************************************************
 * 工程名  :接听电话
 * 描述    :通过STM32开发板控制模块自动接听来电
 * 实验平台:STM32F10X
 * 库版本  :
 * 软件功能说明
   板子上电后运行指示灯RUNING_LED会以一秒的频率闪烁
	 插上耳机，程序运行后可以自动接听来电
**********************************************************************************/

#include "stm32f10x.h"
#include "usart.h"
#include "Led.h"
#include "SysTick.h"
#include "timer.h"
#include "string.h"

#define Buf2_Max 200 //串口2缓存长度
/*************	本地常量声明	**************/

/*************  本地变量声明	**************/
char Uart2_Buf[Buf2_Max]; //串口2接收缓存
u8 First_Int = 0;		  //串口2接收计数器

/*************	本地函数声明	**************/
u8 Find(char *a);	 //查找字符串
void CLR_Buf2(void);  //清除串口2接收缓存
void Wait_CREG(void); //查询等待模块注册成功

/*************  外部函数和变量声明*****************/

/*******************************************************************************
* 函数名 : main 
* 描述   : 主函数
* 输入   : 
* 输出   : 
* 返回   : 
* 注意   : 串口2负责与GPRS模块通信，串口1用于串口调试，可以避免在下载程序时数据
					 还发送到模块
*******************************************************************************/
int main(void)
{
	SysTick_Init_Config();		//系统滴答时钟初始化
	GPIO_Config();				//GPIO端口初始化
	USART2_Init_Config(115200); //串口2初始化
	Timer2_Init_Config();		//定时器2初始化
	Wait_CREG();
	while (1)
	{
		if (Find("CRING") || Find("RING")) //有新来电
		{
			CLR_Buf2();					 //清除串口2接收缓存
			UART2_SendString("ATA\r\n"); //接听来电
		}
		Delay_nMs(1000); //每秒查询一次
	}
}

/*******************************************************************************
* 函数名  : USART2_IRQHandler
* 描述    : 串口1中断服务程序
* 输入    : 无
* 返回    : 无 
* 说明    : 
*******************************************************************************/
void USART2_IRQHandler(void)
{
	u8 Res = 0;
	Res = USART_ReceiveData(USART2);
	Uart2_Buf[First_Int] = Res; //将接收到的字符串存到缓存中
	First_Int++;				//缓存指针向后移动
	if (First_Int > Buf2_Max)   //如果缓存满,将缓存指针指向缓存的首地址
	{
		First_Int = 0;
	}
}

/*******************************************************************************
* 函数名  : TIM2_IRQHandler
* 描述    : 定时器2中断断服务函数
* 输入    : 无
* 输出    : 无
* 返回    : 无 
* 说明    : 无
*******************************************************************************/
void TIM2_IRQHandler(void) //TIM3中断
{
	static u8 flag = 1;

	if (TIM_GetITStatus(TIM2, TIM_IT_Update) != RESET) //检查TIM3更新中断发生与否
	{

		TIM_ClearITPendingBit(TIM2, TIM_IT_Update); //清除TIMx更新中断标志

		if (flag)
		{
			LED4_ON();
			flag = 0;
		}
		else
		{
			LED4_OFF();
			flag = 1;
		}
	}
}
/*******************************************************************************
* 函数名 : Find
* 描述   : 判断缓存中是否含有指定的字符串
* 输入   : 
* 输出   : 
* 返回   : unsigned char:1 找到指定字符，0 未找到指定字符 
* 注意   : 
*******************************************************************************/
u8 Find(char *a)
{
	if (strstr(Uart2_Buf, a) != NULL)
		return 1;
	else
		return 0;
}
/*******************************************************************************
* 函数名 : CLR_Buf2
* 描述   : 清除串口2缓存数据
* 输入   : 
* 输出   : 
* 返回   : 
* 注意   : 
*******************************************************************************/
void CLR_Buf2(void)
{
	u16 k;
	for (k = 0; k < Buf2_Max; k++) //将缓存内容清零
	{
		Uart2_Buf[k] = 0x00;
	}
	First_Int = 0; //接收字符串的起始存储位置
}

/*******************************************************************************
* 函数名 : Wait_CREG
* 描述   : 等待模块注册成功
* 输入   : 
* 输出   : 
* 返回   : 
* 注意   : 
*******************************************************************************/
void Wait_CREG(void)
{
	u8 i;
	u8 k;
	i = 0;
	CLR_Buf2();
	while (i == 0)
	{
		CLR_Buf2();
		UART2_SendString("AT+CREG?"); //查询等待模块是否注册成功
		UART2_SendLR();
		Delay_nMs(5000);
		for (k = 0; k < Buf2_Max; k++)
		{
			if (Uart2_Buf[k] == ':')
			{
				if ((Uart2_Buf[k + 4] == '1') || (Uart2_Buf[k + 4] == '5')) //说明模块注册成功
				{
					i = 1;
					break;
				}
			}
		}
	}
}